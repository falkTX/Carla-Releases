#!/bin/bash

set -e

cd $(dirname ${0})

# ---------------------------------------------------------------------------------------------------------------------

target="${1}"

if [ -z "${target}" ]; then
    echo "usage: ${0} <target>"
    exit 1
fi

# ---------------------------------------------------------------------------------------------------------------------

pushd PawPaw
source local.env ${target}
popd

# ---------------------------------------------------------------------------------------------------------------------

pushd Carla
make features
make EXTERNAL_PLUGINS=false ${MAKE_ARGS}
if [ "${WIN64}" -eq 1 ]; then
make EXTERNAL_PLUGINS=false ${MAKE_ARGS} win32r
fi
make dist
popd

# ---------------------------------------------------------------------------------------------------------------------
